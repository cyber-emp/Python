# dictionaries are indexed by their keys which can be a string or numeric

my_dict = {'apple': 3, 'banana': 5, 'orange': 2}
print(my_dict['apple'])

# dictionaries are mutable, meaning you can change their contents add new key pairs

my_dict['grapes'] = 4
print(my_dict)

my_dict['banana'] = 7
print(my_dict)



